class TokenBucket:
    """Burst-tolerant rate limiter. Pass a monotonic `now` (seconds) to allow()."""

    def __init__(self, rate: float, capacity: float) -> None:
        self.rate = rate
        self.capacity = capacity
        self.tokens = capacity
