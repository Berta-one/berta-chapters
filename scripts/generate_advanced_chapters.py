#!/usr/bin/env python3
"""
Berta Advanced Track Generator (Chapters 16-25)

Builds the full Advanced & Specialization Track from structured per-chapter
specs in ``advanced_specs.SPECS``. For each chapter it emits:

- ``chapters/chapter-NN-slug/`` with README.md, requirements.txt, three
  notebooks, scripts (config + a domain module with real, runnable code),
  two exercise problem sets + solutions (notebooks and a runnable
  ``solutions.py`` used by CI), datasets, and Mermaid diagrams.
- ``docs/chapters/chapter-NN.md`` landing page plus three ``content/chNN-*``
  read-online summaries, all wired so ``mkdocs build --strict`` passes.

The generator is idempotent: re-running it overwrites generated files in
place. It does NOT edit mkdocs.yml, the homepage, CURRICULUM, or berta.py —
those one-off wiring changes are committed alongside.

Usage:
    python scripts/generate_advanced_chapters.py

Generated by Berta AI | Created by Luigi Pascal Rondanini
"""

from __future__ import annotations

import json
from pathlib import Path

from advanced_specs import SPECS

REPO = Path(__file__).resolve().parent.parent
CHAPTERS = REPO / "chapters"
DOCS = REPO / "docs" / "chapters"

NB_META = {
    "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
    "language_info": {"name": "python", "version": "3.11.0"},
}


# --------------------------------------------------------------------------- #
# Low-level notebook helpers
# --------------------------------------------------------------------------- #
def md_cell(src: str) -> dict:
    return {"cell_type": "markdown", "metadata": {}, "source": _lines(src)}


def code_cell(src: str) -> dict:
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": {},
        "outputs": [],
        "source": _lines(src),
    }


def _lines(src: str) -> list[str]:
    """Split source into a list of lines preserving trailing newlines,
    matching the JSON shape Jupyter writes (every line but the last ends \n)."""
    text = src.rstrip("\n")
    parts = text.split("\n")
    return [p + "\n" for p in parts[:-1]] + [parts[-1]] if parts else [""]


def notebook(cells: list[dict]) -> dict:
    return {"cells": cells, "metadata": NB_META, "nbformat": 4, "nbformat_minor": 5}


def write_json(path: Path, obj: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(obj, indent=1) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not text.endswith("\n"):
        text += "\n"
    path.write_text(text, encoding="utf-8")


# --------------------------------------------------------------------------- #
# Content builders
# --------------------------------------------------------------------------- #
def prereq_label(spec: dict) -> str:
    ch = spec["prereqs"]
    return "Chapters " + ", ".join(str(c) for c in ch) if ch else "None"


