def build_notebook(spec: dict, nb: dict) -> dict:
    n = spec["num"]
    learn_rows = "\n".join(f"| {t} | {s} |" for t, s in nb["learn"])
    concepts = "\n".join(f"- {c}" for c in nb["concepts"])
    header = md_cell(
        f"# Chapter {n}: {spec['title']}\n"
        f"## Notebook {nb['file'][:2]} — {nb['title']}\n\n"
        f"{nb['intro']}\n\n"
        f"### What you'll learn\n\n"
        f"| Topic | Section |\n|-------|--------|\n{learn_rows}\n\n"
        f"**Time estimate:** {nb['time']}\n\n"
        f"### Key concepts\n\n{concepts}\n\n"
        f"---\n*Generated by Berta AI*"
    )
    cells = [header]
    for kind, body in nb["cells"]:
        cells.append(code_cell(body) if kind == "code" else md_cell(body))
    cells.append(
        md_cell(
            "---\n\n## Summary\n\n"
            f"{nb['summary']}\n\n"
            "---\n*Generated by Berta AI | Berta Chapters*"
        )
    )
    return notebook(cells)


def build_problem_set(spec: dict, ps: dict, idx: int, solutions: bool) -> dict:
    n = spec["num"]
    kind = "Solutions" if solutions else "Problem Set"
    intro = (
        f"# Chapter {n}: {spec['title']}\n"
        f"## {ps['title']} — {kind}\n\n"
        f"{ps['intro']}\n\n"
        + (
            "Work through each task in order. Try it yourself before opening the "
            "solutions notebook.\n\n"
            if not solutions
            else "Reference solutions with explanations. Compare against your own.\n\n"
        )
        + "---\n*Generated by Berta AI*"
    )
    cells = [md_cell(intro)]
    for j, task in enumerate(ps["tasks"], 1):
        cells.append(md_cell(f"### Task {j}: {task['name']}\n\n{task['desc']}"))
        cells.append(code_cell(task["solution"] if solutions else task["starter"]))
    return notebook(cells)


